/**
 * Write the program to print the difference and product  of the  numbers 45 and 32
 */

package Lets_Program;

public class Assignment_5 {
    public static void main(String[] args) {
        int n1 = 45;
        int n2 = 32;
        int difference = n1 - n2;
        int product = n1 * n2;
        System.out.println("Difference of " + n1 + " and " + n2 + " is " + difference);
        System.out.println("Product of " + n1 + " and " + n2 + " is " + product);
    }
}
