/**
 * Write a program to print the sum of the numbers 2,4 and 5
 */


package Lets_Program;

public class Assignment_4 {
    public static void main(String[] args) {
        int n1 = 2;
        int n2 = 4;
        int n3 = 5;
        int sum = n1 + n2 + n3;
        System.out.println("Sum of the number " + n1 + ", " + n2 + ", " + n3 + " is " + sum);
    }
}
